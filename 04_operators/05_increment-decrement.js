// Increment Operator
// In JavaScript we use the increment operator to increase a value stored in a variable.
// The increment could be pre or post increment. Let us see each of them:

// Pre-increment -- pehle increment karo, fir print karo
let count1 = 0;
console.log(++count1); // 1
console.log(count1); // 1

// Post-increment -- pehle print karo, fir increment karo
let count2 = 0;
console.log(count2++); // 0
console.log(count2); // 1

// We use post-increment most of the time. At least you should remember how to use post-increment operator.

console.log('----------------------------------------------------------------------------------------------------------------------------');

// Decrement Operator
// In JavaScript we use the decrement operator to decrease a value stored in a variable.
// The decrement could be pre or post decrement. Let us see each of them:

// Pre-decrement -- pehle decrement karo, fir print karo
let count3 = 0;
console.log(--count3); // -1
console.log(count3); // -1

// Post-decrement -- pehle print karo, fir decrement karo
let count4 = 0;
console.log(count4--); // 0
console.log(count4); // -1
