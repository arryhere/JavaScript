// JavaScript Assignment Operators
// Assignment operators assign values to JavaScript variables.

// Operator	    Example	    Same As
// =	        x = y	    x = y
// +=	        x += y	    x = x + y
// -=	        x -= y	    x = x - y
// *=	        x *= y	    x = x * y
// /=	        x /= y	    x = x / y
// %=	        x %= y	    x = x % y
// **=	        x **= y	    x = x ** y

let x = 5;
x +=5;
console.log(x);

x -=5;
console.log(x);

x *=5;
console.log(x);

x /=5;
console.log(x);

x **=5;
console.log(x);

x %=5
console.log(x);
